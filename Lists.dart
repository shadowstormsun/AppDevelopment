void main() {
  //List stores multiple data at the same time
  //Index of list always starts with 0
  List<String> users = ["Suneet", "Bhakti", "Satyarth"];
  print(users);
  //Add data to list
  users.add("Satyarth");
  //Remove data from list
  users.remove("Satyarth");
  //Total number of data in list
  //ListName.length
  print(users.length);
}
